    private void setFilmTable(List<Film> listFilm) {
        if (listFilm==null){
            JOptionPane.showMessageDialog(this, "Data film gagal diambil dari database","DATABASE FAILED",JOptionPane.ERROR_MESSAGE);
        }else{
            DefaultTableModel tableModel = new DefaultTableModel();
            tableModel.setColumnIdentifiers(
                new String[] {
                    "Nama Film",
                    "Genre",
                    "Jam Mulai ke-1",
                    "Jam Selesai ke-1",
                    "Jam Mulai ke-2",
                    "Jam Selesai ke-2",
                    "Jam Mulai ke-3",
                    "Jam Selesai ke-3",
                    "Ruang"
                }
            );
            
            for (Film f : listFilm) {
                Object[] o = new Object[10];
                o[0] = f.getName();
                o[1] = f.getGenre();
                o[2] = f.getJamMulai1();
                o[3] = f.getJamSelesai1();
                o[4] = f.getJamMulai2();
                o[5] = f.getJamSelesai2();
                o[6] = f.getJamMulai3();
                o[7] = f.getJamSelesai3();
                o[8] = f.getRuang();
                tableModel.addRow(o);
            }
            film_table.setAutoResizeMode( javax.swing.JTable.AUTO_RESIZE_ALL_COLUMNS );
            film_table.setModel(tableModel);
            film_table.getColumnModel().getColumn(0).setMinWidth(120);
            film_table.getColumnModel().getColumn(2).setMaxWidth(50);
        }
    }
        
                    
