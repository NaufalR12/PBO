    @Override
    public void update(Observable o, Object arg) {
        if (o==filmController){
            if (arg==null){
                JOptionPane.showMessageDialog(this, "Produk gagal diperbaharui karena kegagalan koneksi ke database");
            }else{            
                Film f = (Film) arg;
                if (filmController.getCrudState() == CrudState.INSERT){
                    JOptionPane.showMessageDialog(this, f.getName()+" berhasil ditambahkan");
                }
                setFilmTable(filmController.getAllFilm());
                resetForm(); 
            }            
        }
    }
    
